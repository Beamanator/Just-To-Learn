/**
 * Name: Add
 * Intro Arcade #: 1
 * 
 * Write a function that returns the sum of two numbers.
 * 
 * Example
 * 
 * For param1 = 1 and param2 = 2, the output should be
 * add(param1, param2) = 3.
 * 
 * @param {any} param1 
 * @param {any} param2 
 * @returns 
 */
function add(param1, param2) {
    return (param1 + param2);
}

// also can be written shorter as:
add = (a, b) => a + b
