/**
 * Name: psyOps
 * Reverse Challenge 11 Apr 2018
 * 
 * @param {any} a 
 * @param {any} b 
 * @returns 
 */
psyOps = (a, b) => ''
    + (a + b)
    + (a - b)
    + a * b
    + a / b

